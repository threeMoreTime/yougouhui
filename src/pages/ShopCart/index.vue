          </li>
          <li class="cart-list-con5">
            <a @click="skuNum <= 1 ? 1 : skuNum--" class="mins">-</a>
            <input
              autocomplete="off"
              type="text"
              v-model="skuNum"
              class="itxt"
              @change="changeSkuNum"
            />
            <a @click="skuNum++" class="plus">+</a>
          </li>
          <li class="cart-list-con6">
            <span class="sum">{{ skuNum * cart.skuPrice }}</span>
          </li>
