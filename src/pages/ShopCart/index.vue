        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { mapGetters, mapState } from "vuex";
export default {
  name: "ShopCart",
  data() {
    return {
      skuNum: 1,
    };
  },
  mounted() {
    this.$store.dispatch("reqCartInfo");
    console.log(this.$route);
  },
  computed: {
    // 计算购物车所有商品的总价
    subTotal() {
      let sum = 0;
      this.cartInfoList.forEach((item) => {
        sum += item.skuNum * item.skuPrice;
      });
      return sum;
    },
    // 判断是否全选（返回值1就取消全选 返回值0就全选）
    isALLchecked() {
      return this.cartInfoList.every((item) => item.isChecked == 1);
    },
    ...mapGetters(["cartListInfo", "cartInfoList"]),
    cartInfoList() {
      return this.cartListInfo.cartInfoList || [];
    },
  },
  methods: {
    changeSkuNum(event) {
      // 从事件对象中获取输入字段的值
      let num = parseFloat(event.target.value);
      // 检查是否为 NaN 或小于 1，如果是则将 this.skuNum 设置为 1，否则将其设置为取整后的 num
      this.skuNum = isNaN(num) || num < 1 ? 1 : Math.floor(num);
    },
  },
};
</script>

